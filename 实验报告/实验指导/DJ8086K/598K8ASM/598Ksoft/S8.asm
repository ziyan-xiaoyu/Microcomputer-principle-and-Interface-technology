	CODE SEGMENT        ;S8.ASM
	ASSUME CS:CODE	;MOV RAM:4000-40FFH TO 4100-41FFH
	ORG 2EF0H
START: 	MOV CX,0100H
	MOV SI,4000H
	MOV DI,4100H	;
	CMP SI,DI
	JA FADR		;SI>DI JMP
	ADD SI,CX
	ADD DI,CX
	DEC SI
	DEC DI
CON1:	MOV AL,[SI]
	MOV [DI],AL
	DEC SI
	DEC DI
	DEC CX
	JNE CON1
	JMP $
FADR:	MOV AL,[SI]
	INC SI
	INC DI
	DEC CX
	JNE FADR
	JMP $
	CODE ENDS
END  START

