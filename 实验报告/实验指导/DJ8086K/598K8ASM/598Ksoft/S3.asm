START: 	MOV CX,0004H	;INPUT DATA IN [4000]:01,02,03,04,05,06,07,08(12,34,56,78)
	MOV DI,4000H	;result in [4100]:0C,00,22,00,38,00,4E,00
CON1:	MOV AL,[DI]
	ADD AL,AL	;*2
	MOV BL,AL
	ADD AL,AL	;*2
	ADD AL,AL	;*2
	ADD AL,BL	;*10
	INC DI
	MOV AH,00
	ADD AL,[DI]
	MOV [DI+0FFH],AX
	INC DI
	LOOP CON1
	JMP $
